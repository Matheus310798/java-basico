package exemplo01;

/**
 * Programa para demonstrar a utilização de variáveis
 * 
 * @author mtomazs
 * @since 04/02/2021
 */
public class Variaveis {

	/*
	 * Método principal para executar o programa
	 */

	public static void main(String[] args) {
		
		// declarando variável do tipo inteiro
		int idade;
		
		// declarando variável do tipo String
		String nome;
		
		// declarando variável do tipo double
		double valor;
		
		// atribuindo valores para as variáveis
		idade = 22;
		nome = "Matheus Tomaz da Silva";
		valor = 198.98;
		
		// exibindo o conteúdo das variávei no console
		System.out.println("Variável IDADE: " + idade + ".");
		System.out.println("Variável NOME: " + nome + ".");
		System.out.println("Variável VALOR: " + valor + ".");
		
		

	}

}
