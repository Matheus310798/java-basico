package exercicios;

/**
 * Programa para escrever em ordem decrescente de 10 a 1
 * @author mtomazs
 * @since 10/02/2021
 */
public class Exercicio67 {

	/*
	 * Método principal para executar o programa
	 */
	public static void main(String[] args) {
		
		for (int i = 10; i > 0; i--) {
		System.out.println("Número: " + i);
		}

	}

}
