package exercicios;

/**
 * Apresentar no console nome, celular e cidade com as variáveis atribuidas
 * 
 * @author mtomazs
 * @since 04/02/2021
 */
public class Exercicio04 {

	/*
	 * Método principal para executar o programa
	 */
	public static void main(String[] args) {

		// Declarando variáveis
		String nome, celular, cidade;

		// Atribuindo valores as variáveis
		nome = "Matheus Tomaz";
		celular = "98194-2799";
		cidade = "Barueri";

		// Exibindo no console
		System.out.println("Meu NOME: " + nome);
		System.out.println("Meu CELULAR: " + celular);
		System.out.println("Minha CIDADE: " + cidade);

	}

}
